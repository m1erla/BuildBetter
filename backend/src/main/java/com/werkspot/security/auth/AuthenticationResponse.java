import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;

@Data
@Builder
@AllArgsConstructor
@NoArgsConstructor
public class AuthenticationResponse {
     @JsonIgnoreProperties
     String accessToken;

     @JsonIgnoreProperties
     String refreshToken;
}
